//  Arithmetic Operators
var num1 = 100;
var num2 = 2;

// 1. Addition
var result = num1 + num2; //Output 102

// 2. Subtraction
var result = num1 - num2; //Output 98

// 3. Multiplication
var result = num1 * num2; //Output 200

// 4. Division
var result = num1 + num2; //Output 50

// 5. Modulus
var result = num1 % num2; //Output 0

// 6. Exponential
var result = num1 ** num2; //Output 1000

// 7. Pre Increment
var num = 10;
var result = ++num; // Output num= 11; result = 11

// 8. Post Increment
var num = 10;
var result = num++; // Output num= 10; result = 11

// 9. Pre Decrement
var num = 10;
var result = --num; // Output num= 9; result = 9

// 10. Post Decrement
var num = 10;
var result = num--; // Output num= 10; result = 9
